    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        currentState = "A";
        acceptingStates = Arrays.asList("D", "F");

        String inputString = txtArea2.getText();

        JTextArea outputArea = new JTextArea();
        outputArea.setEditable(false);

        for (char symbol : inputString.toCharArray()) {
            String nextState = transition(String.valueOf(symbol));
            outputArea.append("\nCurrent Input: " + symbol + "\nState: " + nextState + "\n");
            if (nextState == null) {
                outputArea.append(" ");
                break;
            }
        }

        if (isAcceptingState()) {
            outputArea.append("\nSTRING ACCEPTED\n");
        } else {
            outputArea.append("\nSTRING REJECTED\n");
        }

        scrollPane.setViewportView(outputArea);
    }//GEN-LAST:event_btnEnterActionPerformed

