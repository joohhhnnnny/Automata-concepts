        public String transition(String inputSymbol) {
        List<String> nextStates = stateTable.get(currentState).get(inputSymbol);
        if (nextStates == null || nextStates.isEmpty()) {
            return null;
        }

        int nextStateIndex = random.nextInt(nextStates.size());
        currentState = nextStates.get(nextStateIndex);
        return currentState;
    }

    public boolean isAcceptingState() {
        return acceptingStates.contains(currentState);
    }
